-- How many reviews are written per neighborhood?

-- +------------------------+----------+
-- | Albany Park            | 2557     |
-- | Archer Heights         | 541      |
-- | Armour Square          | 4165     |
-- | Ashburn                | 274      |
-- | Auburn Gresham         | 20       |
-- | Austin                 | 1475     |
-- | Avalon Park            | 18       |
-- | Avondale               | 6784     |
-- | Belmont Cragin         | 633      |
-- | Beverly                | 382      |
-- | Bridgeport             | 5559     |
-- | Brighton Park          | 678      |
-- | Burnside               | 10       |

SELECT
    DISTINCT neighborhood,
    COUNT(review_id)
FROM listings AS listings
LEFT JOIN
    (SELECT
        id AS review_id,
        listing_id
    FROM reviews) AS reviews
    ON listings.id = reviews.listing_id
GROUP BY 1;
